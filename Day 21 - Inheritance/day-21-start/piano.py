# slice lists
piano_keys = ["a", "b", "c", "d", "e", "f", "g"]

# printing from 2 to 5 position
print(piano_keys[2:5])

# printing from 2 position to the final
print(piano_keys[2:])

# printing from the start until 5 position
print(piano_keys[:5])

# printing from 2 to 5 position, jumping 2 positions
print(piano_keys[2:5:2])

# printing the list reverse
print(piano_keys[::-1])
